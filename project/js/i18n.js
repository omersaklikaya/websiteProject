function getLang() {
  return document.documentElement.getAttribute("data-lang") || "tr";
}

function setLang(lang) {
  if (!I18N[lang]) return;
  const t = I18N[lang];
  document.documentElement.setAttribute("data-lang", lang);
  document.documentElement.setAttribute("lang", lang);
  document.documentElement.setAttribute("dir", lang === "ar" ? "rtl" : "ltr");
  localStorage.setItem("lang", lang);

  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const k = el.getAttribute("data-i18n");
    if (t[k] != null) el.textContent = t[k];
  });
  applyPlaceholders(t);
  document.querySelectorAll("[data-i18n-aria]").forEach((el) => {
    const k = el.getAttribute("data-i18n-aria");
    if (t[k] != null) el.setAttribute("aria-label", t[k]);
  });

  document.querySelectorAll(".lang-option").forEach((opt) => {
    opt.setAttribute("aria-current", opt.getAttribute("data-lang") === lang ? "true" : "false");
  });
  if (t.page_title) document.title = t.page_title;

  // Dil değişince input/textarea içeriğini temizle; böylece eski alfabe kalmaz
  document.querySelectorAll("input:not([type=hidden]), textarea").forEach((el) => {
    if (el.hasAttribute("data-i18n-placeholder")) {
      el.value = "";
    }
  });

  // dir/lang sonrası placeholder'ların kesin güncellenmesi (RTL/LTR geçişinde bazen atlanıyor)
  requestAnimationFrame(() => {
    applyPlaceholders(t);
    requestAnimationFrame(() => applyPlaceholders(t));
  });
}

function applyPlaceholders(t) {
  document.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
    const k = el.getAttribute("data-i18n-placeholder");
    if (t[k] != null) {
      el.placeholder = "";
      el.placeholder = t[k];
    }
  });
}

function initLang() {
  const saved = localStorage.getItem("lang") || "tr";
  setLang(saved);
  const btn = document.getElementById("langToggle");
  const dropdown = document.getElementById("langDropdown");
  const switchWrap = document.querySelector(".lang-switch");
  if (btn && dropdown && switchWrap) {
    btn.addEventListener("click", (e) => {
      e.stopPropagation();
      const isOpen = switchWrap.classList.toggle("is-open");
      btn.setAttribute("aria-expanded", isOpen);
      dropdown.setAttribute("aria-hidden", !isOpen);
    });
    dropdown.querySelectorAll(".lang-option").forEach((opt) => {
      opt.addEventListener("click", () => {
        const lang = opt.getAttribute("data-lang");
        if (lang) setLang(lang);
        switchWrap.classList.remove("is-open");
        btn.setAttribute("aria-expanded", "false");
        dropdown.setAttribute("aria-hidden", "true");
      });
    });
    document.addEventListener("click", () => {
      switchWrap.classList.remove("is-open");
      btn.setAttribute("aria-expanded", "false");
      dropdown.setAttribute("aria-hidden", "true");
    });
  }
}

if (document.readyState === "loading") {
  document.addEventListener("DOMContentLoaded", initLang);
} else {
  initLang();
}
